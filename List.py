#Create a list
MK_list = [1, 2, 3, 4]

#To add an element in the list
MK_list.append(5)

#To remove an element from the list
MK_list.remove(2)

#Modifying the element
MK_list[0] = 10

#inserting an element (changing the elements value to a new value)
MK_list.insert(2, "TWO")

#To print the list
print("Updated list:", MK_list)
